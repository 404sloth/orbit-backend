class ExecutivePDF(FPDF):
    def __init__(self, template="Executive Gold"):
        super().__init__()
        self.template_name = template
        self.colors = TEMPLATES.get(template, TEMPLATES["Executive Gold"])
        self.set_margins(20, 25, 20)
        self.set_auto_page_break(auto=True, margin=25)

    def header(self):
        # Premium thin bar at the top
        self.set_fill_color(*self.colors["primary"])
        self.rect(0, 0, 210, 3, "F")
        
        if self.page_no() == 1:
            self.set_font("helvetica", "B", 8)
            self.set_text_color(148, 163, 184)
            self.set_y(12)
            self.cell(0, 10, "ORBIT INTELLIGENCE ENGINE  |  CONFIDENTIAL EXECUTIVE AUDIT", align="R")
            self.ln(20)

    def footer(self):
        self.set_y(-20)
        self.set_font("helvetica", "I", 8)
        self.set_text_color(148, 163, 184)
        date_str = datetime.datetime.now().strftime("%B %d, %Y")
        self.cell(0, 10, f"Page {self.page_no()} of {{nb}}  |  {date_str}  |  Generated by Orbit AI", align="C")

def parse_markdown_table(block: str) -> list[list[str]]:
    """Parses a markdown table into a list of rows."""
    lines = block.strip().split('\n')
    table_data = []
    for line in lines:
        line = line.strip()
        if not line or set(line.replace('|', '').replace('-', '').replace(' ', '')) == set():
            continue
        if line.startswith('|'):
            line = line[1:]
        if line.endswith('|'):
            line = line[:-1]
        row = [cell.strip() for cell in line.split('|')]
        table_data.append(row)
    return table_data

def generate_pdf(doc_type, title, subtitle, content_markdown, filepath, template_name="Executive Gold"):
    """
    Advanced Executive PDF Engine.
    Handles 'Responsive' Tables: 
    - Horizontal for <= 5 columns.
    - Vertical Key-Value pairs for > 5 columns (prevents overflow).
    """
    pdf = ExecutivePDF(template=template_name)
    colors = pdf.colors
    pdf.alias_nb_pages()
    pdf.add_page()
    
    # 1. Premium Cover Header
    pdf.set_font("helvetica", "B", 26)
    pdf.set_text_color(*colors["primary"])
    pdf.multi_cell(0, 14, title.upper(), align="L")
    
    if subtitle:
        pdf.set_font("helvetica", "B", 14)
        pdf.set_text_color(*colors["accent"])
        pdf.cell(0, 10, subtitle, ln=1, align="L")
        
    pdf.ln(5)
    pdf.set_draw_color(*colors["accent"])
    pdf.set_line_width(1)
    pdf.line(20, pdf.get_y(), 80, pdf.get_y())
    pdf.ln(15)
    
    # 2. Content Engine
    import re
    # Convert markdown bold to PDF compatible (subtle)
    content_markdown = re.sub(r'\*\*(.*?)\*\*', r'__BOLD__\1__BOLD__', content_markdown)
    blocks = content_markdown.split("\n\n")
    
    for block in blocks:
        block = block.strip()
        if not block: continue
        
        # --- Advanced Table Logic ---
        if "|" in block and "---" in block:
            table_data = parse_markdown_table(block)
            if not table_data: continue
            
            headers = table_data[0]
            rows = table_data[1:]
            col_count = len(headers)
            
            # DECISION: Horizontal or Vertical?
            if col_count <= 5:
                # HORIZONTAL RENDER
                pdf.set_font("helvetica", "B", 9)
                pdf.set_fill_color(*colors["highlight"])
                pdf.set_text_color(*colors["primary"])
                
                col_width = pdf.epw / col_count
                for h in headers:
                    pdf.cell(col_width, 10, str(h).upper(), border=1, fill=True, align="C")
                pdf.ln()
                
                pdf.set_font("helvetica", "", 8)
                pdf.set_text_color(*colors["text"])
                for row in rows:
                    # Calculate max height needed for this row
                    max_h = 8
                    for cell in row:
                        # Estimate lines
                        lines = pdf.get_string_width(str(cell)) / col_width
                        max_h = max(max_h, (int(lines) + 1) * 5)
                    
                    if pdf.get_y() + max_h > 270: pdf.add_page()
                    
                    for cell in row:
                        x, y = pdf.get_x(), pdf.get_y()
                        pdf.multi_cell(col_width, 5, str(cell).strip(), border=1, align="L")
                        pdf.set_xy(x + col_width, y)
                    pdf.ln(max_h)
            else:
                # VERTICAL RENDER (The "No Space" Fix)
                pdf.set_font("helvetica", "B", 10)
                pdf.set_text_color(*colors["accent"])
                pdf.cell(0, 10, "DATA SUMMARY (Vertical Layout)", ln=1)
                
                for i, row in enumerate(rows):
                    pdf.set_fill_color(*colors["highlight"])
                    pdf.set_font("helvetica", "B", 8)
                    pdf.set_text_color(*colors["primary"])
                    pdf.cell(0, 7, f"ENTRY #{i+1}", ln=1, fill=True)
                    
                    pdf.set_font("helvetica", "", 8)
                    for h, val in zip(headers, row):
                        pdf.set_font("helvetica", "B", 8)
                        pdf.set_text_color(*colors["primary"])
                        pdf.cell(40, 6, f"{h}:", ln=0)
                        pdf.set_font("helvetica", "", 8)
                        pdf.set_text_color(*colors["text"])
                        pdf.multi_cell(0, 6, str(val).strip())
                    pdf.ln(3)
            pdf.ln(5)
            continue

        # --- Headers ---
        if block.startswith("#"):
            level = len(re.match(r'^#+', block).group())
            text = block.replace("#", "").strip()
            
            if level == 1:
                pdf.set_font("helvetica", "B", 18)
                pdf.set_text_color(*colors["primary"])
                pdf.ln(8)
                pdf.cell(0, 12, text, ln=1)
            elif level == 2:
                pdf.set_font("helvetica", "B", 14)
                pdf.set_text_color(*colors["primary"])
                pdf.ln(5)
                pdf.cell(0, 10, text, ln=1)
            else:
                pdf.set_font("helvetica", "B", 11)
                pdf.set_text_color(*colors["accent"])
                pdf.ln(3)
                pdf.cell(0, 8, text, ln=1)
            continue

        # --- Standard Paragraph ---
        pdf.set_font("helvetica", "", 10)
        pdf.set_text_color(*colors["text"])
        # Simple Bold Replacement
        clean_block = block.replace("__BOLD__", "")
        pdf.multi_cell(0, 6, clean_block)
        pdf.ln(3)
            
    pdf.output(filepath)

def generate_docx(doc_type, title, subtitle, content_markdown, filepath):
    """Professional DOCX Export."""
    from docx import Document
    from docx.shared import Pt, RGBColor
    
    document = Document()
    
    title_pg = document.add_heading(title.upper(), 0)
    title_pg.style.font.color.rgb = RGBColor(15, 23, 42)
    
    if subtitle:
        p = document.add_paragraph()
        runner = p.add_run(subtitle)
        runner.font.size = Pt(14)
        runner.font.color.rgb = RGBColor(197, 165, 114)
        
    document.add_paragraph()
    
    blocks = content_markdown.split("\n\n")
    for block in blocks:
        block = block.strip()
        if not block: continue
        
        if "|" in block and "---" in block:
            table_data = parse_markdown_table(block)
            if table_data and len(table_data) > 0:
                table = document.add_table(rows=1, cols=len(table_data[0]))
                table.style = 'Table Grid'
                hdr_cells = table.rows[0].cells
                for i, header in enumerate(table_data[0]):
                    if i < len(hdr_cells):
                        hdr_cells[i].text = header
                for row in table_data[1:]:
                    row_cells = table.add_row().cells
                    for i, cell in enumerate(row):
                        if i < len(row_cells):
                            row_cells[i].text = cell
            continue
            
        if block.startswith("#"):
            level = len(block) - len(block.lstrip('#'))
            text = block.replace("#", "").strip()
            document.add_heading(text, level=min(level, 9))
            continue
            
        document.add_paragraph(block.replace('__BOLD__', ''))
        
    document.save(filepath)

