def generate_excel(content_markdown, filepath):
    """Professional Excel Export with OpenPyXL Styling."""
    import pandas as pd
    from openpyxl.styles import Font, PatternFill, Border, Side, Alignment
    
    blocks = content_markdown.split("\n\n")
    with pd.ExcelWriter(filepath, engine='openpyxl') as writer:
        sheet_count = 0
        for block in blocks:
            if "|" in block and "---" in block:
                table_data = parse_markdown_table(block)
                if len(table_data) > 1:
                    df = pd.DataFrame(table_data[1:], columns=table_data[0])
                    sheet_name = f"Data_{sheet_count + 1}"
                    df.to_excel(writer, sheet_name=sheet_name, index=False)
                    
                    # Apply Styling
                    ws = writer.sheets[sheet_name]
                    header_fill = PatternFill(start_color="0F172A", end_color="0F172A", fill_type="solid")
                    header_font = Font(color="FFFFFF", bold=True)
                    
                    for cell in ws[1]:
                        cell.fill = header_fill
                        cell.font = header_font
                        cell.alignment = Alignment(horizontal="center")
                    
                    # Auto-adjust column widths
                    for col in ws.columns:
                        max_length = 0
                        column = col[0].column_letter
                        for cell in col:
                            try:
                                if len(str(cell.value)) > max_length:
                                    max_length = len(str(cell.value))
                            except: pass
                        adjusted_width = (max_length + 2)
                        ws.column_dimensions[column].width = min(adjusted_width, 50)
                    
                    sheet_count += 1
        
        if sheet_count == 0:
            pd.DataFrame({"Report Summary": [content_markdown]}).to_excel(writer, sheet_name="Report", index=False)

@tool(args_schema=GenerateReportSchema)
def generate_executive_report(doc_type: str, title: str, content_markdown: str, subtitle: str = None, format: str = "PDF", template_name: str = "Executive Gold", config: RunnableConfig = None) -> str:
    """
    Generates high-end executive reports in PDF, IMAGE, DOCX, or EXCEL format.
    The system uses responsive layout engines to handle large datasets without failing.
    """
    try:
        # Extract user context from config if available
        user_id = "unknown"
        if config:
            # If config is RunnableConfig or dict
            if hasattr(config, "get"):
                user_id = str(config.get("configurable", {}).get("user_id", "unknown"))
            elif isinstance(config, dict):
                user_id = str(config.get("configurable", {}).get("user_id", "unknown"))

        output_dir = REPORTS_TEMP_DIR
        os.makedirs(output_dir, exist_ok=True)
        
        safe_title = "".join(x for x in title if x.isalnum() or x in " -_").strip().replace(" ", "_")
        ext = format.lower()
        if ext == "excel": ext = "xlsx"
        if ext == "image": ext = "png"
        
        # Prefix filename with user_id for isolation
        filename = f"{user_id}_{safe_title}_{uuid.uuid4().hex[:6]}.{ext}"
        filepath = os.path.join(output_dir, filename)
        
        if format.upper() == "PDF":
            generate_pdf(doc_type, title, subtitle, content_markdown, filepath, template_name)
        elif format.upper() == "IMAGE":
            generate_image(title, subtitle, content_markdown, filepath, template_name)
        elif format.upper() == "EXCEL":
            generate_excel(content_markdown, filepath)
        elif format.upper() == "DOCX":
            generate_docx(doc_type, title, subtitle, content_markdown, filepath)
        else:
            return f"Error: Unsupported format '{format}'"
            
        download_url = f"http://localhost:8000/reports/download/{filename}"
        display_title = title if title and title.strip() else "Orbit Executive Report"
        return f"A premium **{template_name}** {format} has been generated: **[{display_title}]({download_url})**. Access it via the Executive Artifacts panel."

    except Exception as e:
        logger.error("Advanced Document Generation Failed", error=str(e))
        return f"Error: System failed to render the report. Reason: {str(e)}"


